package Day7_2nd.Review.operator;

public class RelationalDemo {

	public static void main(String[] args) {
		
		//  Relational Operator
		/*
		 * ->
		 * <
		 * >=
		 * <=
		 * 
		 * == means equals;
		 * != means not equal
		 */
		
		System.out.println(10 != 4);
		// == , != can use to compare primitive data type
		
		char ch1 = 'a';
		char ch2 = '2';
		System.out.println(ch1 == ch2);

	}

}
